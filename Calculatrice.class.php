<?php

class Calculatrice {

	public static function additionner($a, $b)
	{
		return ($a+$b);
	}
	
	public static function soustraire($a, $b)
	{
		return ($a-$b);
	}
	
	public static function multiplier($a, $b)
	{
		return ($a*$b);
	}
	
	public static function diviser($a, $b)
	{
		return ($a/$b);
	}

}